/*
 *  Send incoming sensor data every 30 seconds
 */
void TIM3_IRQHandler(void)
{
	TIM3->SR &= ~(TIM_SR_UIF);//timer3 clear flag
	timer3_disable();
	while (!bmp280_read_float(&bmp280, &temperature, &pressure, &humidity));
	sprintf((char *)Data,"AT+S.HTTPGET=192.168.1.30,/node2/save?temperature=%.2f&pressure=%.2f&humidity=%.1f,3000\r",temperature,pressure,humidity);
	Uart_sendstring(Data, lora_uart);
	while(!(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_4))); // wait for PA4 => AUX = 1
	timer3_enable();


}

int main(void)
{

  HAL_Init();


  SystemClock_Config();


  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART1_UART_Init();
  MX_USART3_UART_Init();

  Ringbuf_init();

  bmp280_init_default_params(&bmp280.params);
  bmp280.addr = BMP280_I2C_ADDRESS_0;
  bmp280.i2c = &hi2c1;

  while (!bmp280_init(&bmp280, &bmp280.params)) ;

  while(!(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_4))); // wait for PA4 => AUX = 1

  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_8, GPIO_PIN_RESET); //M0 = 0 NORMAL MODE
  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_8, GPIO_PIN_RESET); //M1 = 0
  HAL_Delay(10);

  timer3_init();
  timer3_enable();

  while (1)
  {
	  /*
	   *
	   * If there is incoming data, receive it
	   * Do the task if there is a relevant command
	   * Send with LoRa if not related command ( mesh networking )
	   *
	   */

	  if(IsDataAvailable(lora_uart))
	  {
		  for(int i=0;i<94;++i)
		  {
			  while (!(IsDataAvailable(lora_uart)));
			  buffertosave[i] = Uart_read(lora_uart);
			  if(Look_for("two-run",buffertosave) == 1)
			  {
				  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, GPIO_PIN_SET);
				  memset(buffertosave,0,128);
				  break;
			  }
			  else if(Look_for("two-stop",buffertosave) == 1)
			  {

				  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, GPIO_PIN_RESET);
				  memset(buffertosave,0,128);
				  break;
			  }
		  }
		  Uart_sendstring(buffertosave, lora_uart);
		  while(!(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_4))); // wait for PA4 => AUX = 1

	  }

  }

}

